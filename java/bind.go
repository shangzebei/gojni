type nativeWarp struct {
	sCls    string
	jCls    jni.Jclass
	env     jni.Env
	natives []jni.JNINativeMethod
}

type method struct {
	fn  interface{}
	sig []args
}

type args struct {
	jSig string
	gSig reflect.Type
}

